package study.ch07.ex;

public class Ex21 {
    public static void main(String[] args) {
        // 문제 21. 구구단 출력
        // for 문을 사용하여 7단을 출력하는 코드를 작성하시오.
        for (int i = 1; i <= 9; i++) {
            System.out.println("7 x " + i + " = " + (7 * i));
        }
        // 출력 형식: 출력 형식: 7 x 1 = 7, 7 x 2 = 14, ...
    }
}
